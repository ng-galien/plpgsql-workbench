import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..");
const schemaPath = resolve(repoRoot, "runtime/sdui/schema/view.schema.json");
const sqlPath = resolve(repoRoot, "runtime/sdui/src/view_schema.sql");
const tsPaths = [
  resolve(repoRoot, "app/src/lib/generated/sdui-contract.ts"),
  resolve(repoRoot, "src/core/plx/generated/sdui-contract.ts"),
];

const schema = JSON.parse(await readFile(schemaPath, "utf8"));

const viewFieldTypes = schema.properties.template.properties.compact.properties.fields.items.oneOf[1].properties.type.enum;
const viewFieldPropertyKeys = Object.keys(
  schema.properties.template.properties.compact.properties.fields.items.oneOf[1].properties,
);
const formFieldTypes = schema.properties.template.properties.form.properties.sections.items.properties.fields.items.properties.type.enum;
const formFieldPropertyKeys = Object.keys(
  schema.properties.template.properties.form.properties.sections.items.properties.fields.items.properties,
);
const actionVariants = schema.properties.actions.additionalProperties.properties.variant.enum;
const actionPropertyKeys = Object.keys(schema.properties.actions.additionalProperties.properties);
const entityTypes = schema.properties.entity_type.enum;

function union(values) {
  return values.map((value) => JSON.stringify(value)).join(" | ");
}

const sql = `CREATE OR REPLACE FUNCTION sdui.view_schema()
 RETURNS json
 LANGUAGE sql
 IMMUTABLE
AS $function$
  SELECT '${JSON.stringify(schema, null, 2).replace(/'/g, "''")}'::json;
$function$;
`;

const ts = `// Generated by scripts/generate-sdui-contract.mjs from runtime/sdui/schema/view.schema.json

export const sduiViewSchema = ${JSON.stringify(schema, null, 2)} as const;

export const sduiViewFieldTypes = ${JSON.stringify(viewFieldTypes)} as const;
export const sduiViewFieldPropertyKeys = ${JSON.stringify(viewFieldPropertyKeys)} as const;
export const sduiFormFieldTypes = ${JSON.stringify(formFieldTypes)} as const;
export const sduiFormFieldPropertyKeys = ${JSON.stringify(formFieldPropertyKeys)} as const;
export const sduiActionVariants = ${JSON.stringify(actionVariants)} as const;
export const sduiActionPropertyKeys = ${JSON.stringify(actionPropertyKeys)} as const;

export type SduiEntityType = ${union(entityTypes)};
export type SduiViewFieldType = ${union(viewFieldTypes)};
export type SduiFormFieldType = ${union(formFieldTypes)};
export type SduiActionVariant = ${union(actionVariants)};

export interface SduiViewFieldObject {
  key: string;
  type?: SduiViewFieldType;
  label?: string;
}

export type SduiViewField = string | SduiViewFieldObject;

export interface SduiStatDef {
  key: string;
  label: string;
  variant?: string;
}

export interface SduiRelatedDef {
  entity: string;
  label: string;
  filter: string;
}

export interface SduiTemplateLevel {
  fields: SduiViewField[];
  stats?: SduiStatDef[];
  related?: SduiRelatedDef[];
}

export interface SduiFormField {
  key: string;
  type: SduiFormFieldType;
  label: string;
  required?: boolean;
  options?: unknown;
  source?: string;
  display?: string;
  filter?: string;
}

export interface SduiFormSection {
  label: string;
  fields: SduiFormField[];
}

export interface SduiActionMeta {
  label: string;
  icon?: string;
  variant?: SduiActionVariant;
  confirm?: string;
}

export interface SduiViewTemplate {
  uri?: string;
  label?: string;
  icon?: string;
  readonly?: boolean;
  entity_type?: SduiEntityType;
  template?: {
    compact?: SduiTemplateLevel;
    standard?: SduiTemplateLevel;
    expanded?: SduiTemplateLevel;
    form?: {
      sections: SduiFormSection[];
    };
  };
  actions?: Record<string, SduiActionMeta>;
}
`;

await writeFile(sqlPath, sql);
for (const tsPath of tsPaths) {
  await mkdir(dirname(tsPath), { recursive: true });
  await writeFile(tsPath, ts);
}
