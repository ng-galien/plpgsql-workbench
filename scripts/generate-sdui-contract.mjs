import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..");
const viewSchemaPath = resolve(repoRoot, "runtime/sdui/schema/view.schema.json");
const uiNodeSchemaPath = resolve(repoRoot, "runtime/sdui/schema/ui-node.schema.json");
const viewSqlPath = resolve(repoRoot, "runtime/sdui/src/view_schema.sql");
const uiNodeSqlPath = resolve(repoRoot, "runtime/sdui/src/ui_node_schema.sql");
const tsPaths = [
  resolve(repoRoot, "app/src/lib/generated/sdui-contract.ts"),
  resolve(repoRoot, "src/core/plx/generated/sdui-contract.ts"),
];

const viewSchema = JSON.parse(await readFile(viewSchemaPath, "utf8"));
const uiNodeSchema = JSON.parse(await readFile(uiNodeSchemaPath, "utf8"));

const viewFieldTypes =
  viewSchema.properties.template.properties.compact.properties.fields.items.oneOf[1].properties.type.enum;
const viewFieldPropertyKeys = Object.keys(
  viewSchema.properties.template.properties.compact.properties.fields.items.oneOf[1].properties,
);
const formFieldTypes =
  viewSchema.properties.template.properties.form.properties.sections.items.properties.fields.items.properties.type.enum;
const formFieldPropertyKeys = Object.keys(
  viewSchema.properties.template.properties.form.properties.sections.items.properties.fields.items.properties,
);
const actionVariants = viewSchema.properties.actions.additionalProperties.properties.variant.enum;
const actionPropertyKeys = Object.keys(viewSchema.properties.actions.additionalProperties.properties);
const entityTypes = viewSchema.properties.entity_type.enum;
const uiNodeTypes = uiNodeSchema.oneOf.map((entry) => entry.properties.type.const);

function union(values) {
  return values.map((value) => JSON.stringify(value)).join(" | ");
}

const viewSql = `CREATE OR REPLACE FUNCTION sdui.view_schema()
 RETURNS json
 LANGUAGE sql
 IMMUTABLE
AS $function$
  SELECT '${JSON.stringify(viewSchema, null, 2).replace(/'/g, "''")}'::json;
$function$;
`;

const uiNodeSql = `CREATE OR REPLACE FUNCTION sdui.ui_node_schema()
 RETURNS json
 LANGUAGE sql
 IMMUTABLE
AS $function$
  SELECT '${JSON.stringify(uiNodeSchema, null, 2).replace(/'/g, "''")}'::json;
$function$;
`;

const ts = `// Generated by scripts/generate-sdui-contract.mjs from runtime/sdui/schema/*.json

export const sduiViewSchema = ${JSON.stringify(viewSchema, null, 2)} as const;
export const sduiUiNodeSchema = ${JSON.stringify(uiNodeSchema, null, 2)} as const;

export const sduiViewFieldTypes = ${JSON.stringify(viewFieldTypes)} as const;
export const sduiViewFieldPropertyKeys = ${JSON.stringify(viewFieldPropertyKeys)} as const;
export const sduiFormFieldTypes = ${JSON.stringify(formFieldTypes)} as const;
export const sduiFormFieldPropertyKeys = ${JSON.stringify(formFieldPropertyKeys)} as const;
export const sduiActionVariants = ${JSON.stringify(actionVariants)} as const;
export const sduiActionPropertyKeys = ${JSON.stringify(actionPropertyKeys)} as const;
export const sduiUiNodeTypes = ${JSON.stringify(uiNodeTypes)} as const;

export type SduiEntityType = ${union(entityTypes)};
export type SduiViewFieldType = ${union(viewFieldTypes)};
export type SduiFormFieldType = ${union(formFieldTypes)};
export type SduiActionVariant = ${union(actionVariants)};
export type SduiUiNodeType = ${union(uiNodeTypes)};

export interface SduiViewFieldObject {
  key: string;
  type?: SduiViewFieldType;
  label?: string;
}

export type SduiViewField = string | SduiViewFieldObject;

export interface SduiStatDef {
  key: string;
  label: string;
  variant?: string;
}

export interface SduiRelatedDef {
  entity: string;
  label: string;
  filter: string;
}

export interface SduiTemplateLevel {
  fields: SduiViewField[];
  stats?: SduiStatDef[];
  related?: SduiRelatedDef[];
}

export interface SduiFormField {
  key: string;
  type: SduiFormFieldType;
  label: string;
  required?: boolean;
  search?: boolean;
  options?: unknown;
  source?: string;
  display?: string;
  filter?: string;
}

export interface SduiFormSection {
  label: string;
  fields: SduiFormField[];
}

export interface SduiActionMeta {
  label: string;
  icon?: string;
  variant?: SduiActionVariant;
  confirm?: string;
}

export interface SduiUiColumnNode {
  type: "column";
  children: SduiUiNode[];
}

export interface SduiUiRowNode {
  type: "row";
  children: SduiUiNode[];
}

export interface SduiUiSectionNode {
  type: "section";
  label: string;
  children: SduiUiNode[];
}

export interface SduiUiFieldNode {
  type: "field";
  field: SduiFormField;
}

export interface SduiUiHeadingNode {
  type: "heading";
  text: string;
  level?: number;
}

export interface SduiUiTextNode {
  type: "text";
  value: string;
}

export interface SduiUiBadgeNode {
  type: "badge";
  text: string;
  variant?: string;
}

export interface SduiUiColorNode {
  type: "color";
  value: string;
}

export interface SduiUiMarkdownNode {
  type: "md";
  content: string;
}

export interface SduiUiStatNode {
  type: "stat";
  value: string;
  label: string;
  variant?: string;
}

export interface SduiUiCurrencyNode {
  type: "currency";
  amount: number;
  currency?: string;
}

export interface SduiUiWorkflowNode {
  type: "workflow";
  states: string[];
  current: string;
}

export interface SduiUiTimelineEvent {
  date: string;
  label: string;
  variant?: string;
  icon?: string;
}

export interface SduiUiTimelineNode {
  type: "timeline";
  events: SduiUiTimelineEvent[];
}

export interface SduiUiDetailNode {
  type: "detail";
  source: string;
  fields: SduiViewField[];
}

export interface SduiUiColumnDef {
  key: string;
  label: string;
  type?: string;
  align?: "left" | "center" | "right";
}

export interface SduiUiTableNode {
  type: "table";
  source: string;
  columns: SduiUiColumnDef[];
}

export interface SduiUiLineItemsNode {
  type: "line_items";
  source: string;
  columns: SduiUiColumnDef[];
  totals?: Record<string, number | undefined>;
}

export interface SduiUiActionNode {
  type: "action";
  label: string;
  verb: string;
  uri: string;
  variant?: SduiActionVariant;
  confirm?: string;
}

export type SduiUiNode =
  | SduiUiColumnNode
  | SduiUiRowNode
  | SduiUiSectionNode
  | SduiUiFieldNode
  | SduiUiHeadingNode
  | SduiUiTextNode
  | SduiUiBadgeNode
  | SduiUiColorNode
  | SduiUiMarkdownNode
  | SduiUiStatNode
  | SduiUiCurrencyNode
  | SduiUiWorkflowNode
  | SduiUiTimelineNode
  | SduiUiDetailNode
  | SduiUiTableNode
  | SduiUiLineItemsNode
  | SduiUiActionNode;

export interface SduiViewTemplate {
  uri?: string;
  label?: string;
  icon?: string;
  readonly?: boolean;
  entity_type?: SduiEntityType;
  template?: {
    compact?: SduiTemplateLevel;
    standard?: SduiTemplateLevel;
    expanded?: SduiTemplateLevel;
    form?: {
      sections: SduiFormSection[];
    };
  };
  actions?: Record<string, SduiActionMeta>;
}
`;

await writeFile(viewSqlPath, viewSql);
await writeFile(uiNodeSqlPath, uiNodeSql);
for (const tsPath of tsPaths) {
  await mkdir(dirname(tsPath), { recursive: true });
  await writeFile(tsPath, ts);
}
