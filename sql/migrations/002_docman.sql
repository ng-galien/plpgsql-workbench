-- docman: document management tables

CREATE TABLE IF NOT EXISTS docman.label (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  kind text NOT NULL DEFAULT 'tag',
  parent_id integer REFERENCES docman.label(id),
  description text,
  aliases text[] DEFAULT '{}',
  CONSTRAINT label_kind_check CHECK (kind IN ('category', 'tag')),
  UNIQUE (name, kind, parent_id)
);
CREATE INDEX IF NOT EXISTS idx_label_kind ON docman.label (kind);

CREATE TABLE IF NOT EXISTS docman.entity (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  kind text NOT NULL,
  name text NOT NULL,
  aliases text[] DEFAULT '{}',
  metadata jsonb DEFAULT '{}',
  UNIQUE (kind, name)
);
CREATE INDEX IF NOT EXISTS idx_entity_kind ON docman.entity (kind);

CREATE TABLE IF NOT EXISTS docman.document (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  file_path text NOT NULL UNIQUE REFERENCES docstore.file(path),
  doc_type text,
  document_date date,
  source text NOT NULL DEFAULT 'filesystem',
  source_ref text,
  summary text,
  summary_tsv tsvector GENERATED ALWAYS AS (to_tsvector('french', coalesce(summary, ''))) STORED,
  classified_at timestamptz,
  created_at timestamptz DEFAULT now(),
  CONSTRAINT document_source_check CHECK (source IN ('filesystem', 'email'))
);
CREATE INDEX IF NOT EXISTS idx_doc_type ON docman.document (doc_type);
CREATE INDEX IF NOT EXISTS idx_doc_source ON docman.document (source);
CREATE INDEX IF NOT EXISTS idx_doc_date ON docman.document (document_date);
CREATE INDEX IF NOT EXISTS idx_doc_summary_fts ON docman.document USING gin (summary_tsv);

CREATE TABLE IF NOT EXISTS docman.document_label (
  document_id uuid NOT NULL REFERENCES docman.document(id) ON DELETE CASCADE,
  label_id integer NOT NULL REFERENCES docman.label(id) ON DELETE CASCADE,
  confidence real DEFAULT 1.0,
  assigned_by text DEFAULT 'agent',
  assigned_at timestamptz DEFAULT now(),
  PRIMARY KEY (document_id, label_id),
  CONSTRAINT document_label_assigned_by_check CHECK (assigned_by IN ('agent', 'user'))
);

CREATE TABLE IF NOT EXISTS docman.document_entity (
  document_id uuid NOT NULL REFERENCES docman.document(id) ON DELETE CASCADE,
  entity_id integer NOT NULL REFERENCES docman.entity(id) ON DELETE CASCADE,
  role text NOT NULL,
  confidence real DEFAULT 1.0,
  assigned_by text DEFAULT 'agent',
  assigned_at timestamptz DEFAULT now(),
  PRIMARY KEY (document_id, entity_id, role),
  CONSTRAINT document_entity_assigned_by_check CHECK (assigned_by IN ('agent', 'user'))
);

CREATE TABLE IF NOT EXISTS docman.document_relation (
  source_id uuid NOT NULL REFERENCES docman.document(id) ON DELETE CASCADE,
  target_id uuid NOT NULL REFERENCES docman.document(id) ON DELETE CASCADE,
  kind text NOT NULL,
  confidence real DEFAULT 1.0,
  assigned_by text DEFAULT 'agent',
  assigned_at timestamptz DEFAULT now(),
  PRIMARY KEY (source_id, target_id, kind),
  CONSTRAINT document_relation_check CHECK (source_id <> target_id),
  CONSTRAINT document_relation_assigned_by_check CHECK (assigned_by IN ('agent', 'user'))
);

